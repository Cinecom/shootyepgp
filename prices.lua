local progress_scaling = {
  ["T3.5"] = {["T3.5"]=1, ["T3"]=1, ["T2.5"]=1, ["T2"]=1, ["T1.5"]=1, ["T1"]=1},
  ["T3"] =   {["T3.5"]=1, ["T3"]=1, ["T2.5"]=1, ["T2"]=1, ["T1.5"]=1, ["T1"]=1},
  ["T2.5"] = {["T3.5"]=1, ["T3"]=1, ["T2.5"]=1, ["T2"]=1, ["T1.5"]=1, ["T1"]=1},
  ["T2"] =   {["T3.5"]=1, ["T3"]=1, ["T2.5"]=1, ["T2"]=1, ["T1.5"]=1, ["T1"]=1},
  ["T1"] =   {["T3.5"]=1, ["T3"]=1, ["T2.5"]=1, ["T2"]=1, ["T1.5"]=1, ["T1"]=1}
}
local function get_adjusted_price(price,tier,progress)
  if not progress_scaling[progress] then return price end
  if not progress_scaling[progress][tier] then return price end
  return math.floor(progress_scaling[progress][tier] * price)
end

sepgp_prices = sepgp:NewModule("sepgp_prices", "AceDB-2.0")

function sepgp_prices:GetPrice(item,progress)
  if not progress then progress = "T3" end
  local itemID,found,_,itemString,data,tier,is,il
  local price
  if (type(item)=="number") then
    itemID = tonumber(item)
  end
  if (type(item)=="string") then
    found,_,is = string.find(item,"^item:(%d+):.+")
    if (found) then 
      itemID = tonumber(is) 
    else
      found,_,il = string.find(item,"^|c%x+|Hitem:(%d+):.+")
      if (found) then
        itemID = tonumber(il)
      end
    end
  end
  if (itemID) then
    data = prices[itemID]
    if (data) then
      price, tier = data[1], data[2]
      price = get_adjusted_price(price,tier,progress)
    else
      return
    end
  end
  return price
end

